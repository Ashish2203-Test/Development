def add(a,b):
   print ("ADDING %d + %d" % (a,b))
   return a+b
   
def subtract(a,b):
   print("SUBTRACT %d - %d" % (a,b))
   return a-b
   
def multiply(a,b):
   print("MULTIPLY %d * %d" % (a,b))
   return a*b
   
def divide(a,b):
   print("DIVIDE %d / %d" % (a,b))
   return a/b
   
   
